#Operadores
print("#Operadores\n")
x = int(input('Digite um número inteiro x:'))
y = int(input('Digite um número inteiro y:'))

print('A soma de x e y será:', x + y)
print('A subtração entre x e y será:', x - y)
print('A multiplicação de x e y será:', x*y)
print('A divisão entre x e y será:', x/y)
print('A divisão inteira entre x e y será:', x // y)
print('O resto da divisão entre x e y será:', x % y)
print('x elevado a y será:', x**y, "\n")

